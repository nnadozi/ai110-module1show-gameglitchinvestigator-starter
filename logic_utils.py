# Compares guess to secret and returns (outcome, message).
def check_guess(guess, secret):
    """
    Compare guess to secret and return (outcome, message).
    outcome examples: "Win", "Too High", "Too Low"
    """
    if guess == secret:
        return "Win", "🎉 Correct!"

    try:
        if guess > secret:
            return "Too High", "📉 Go LOWER!"  # Corrected: Too High means guess is above secret
        else:
            return "Too Low", "📈 Go HIGHER!"  # Too Low means guess is below secret
    except TypeError:
        # Fallback for type issues: compare as strings
        g = str(guess)
        s = str(secret)
        if g == s:
            return "Win", "🎉 Correct!"
        if g > s:
            return "Too High", "📉 Go LOWER!"
        return "Too Low", "📈 Go HIGHER!"


